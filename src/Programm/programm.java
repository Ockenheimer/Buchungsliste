    public void aktuallisiereAnzeige() {
        jTextFieldName.setText("");
        jTextFieldBett.setText("");
        jTextFieldBettBestaetigt.setText("");
        DefaultComboBoxModel model = new DefaultComboBoxModel(buchungsliste.toArray());
        jListAngefragt.setModel(model);
        jTextAreaBestaetigt.setText(zugesagt.toString());
    }

