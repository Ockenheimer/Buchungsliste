    private void jToggleButtonAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButtonAddActionPerformed
        try {
            String name = jTextFieldName.getText();
            int bett = Integer.valueOf(jTextFieldBett.getText());
            buchungsliste.add(new Gast(name, bett));
            aktuallisiereAnzeige();
        } catch (GastExceptions ex) {
            Logger.getLogger(programm.class.getName()).log(Level.SEVERE, null, ex);
        }


    }//GEN-LAST:event_jToggleButtonAddActionPerformed

    private void jButtonBestaetigtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBestaetigtActionPerformed
        try {
            int a = jListAngefragt.getSelectedIndex();
            String name = buchungsliste.get(a).getName();
            int bett = Integer.valueOf(jTextFieldBettBestaetigt.getText());
            zugesagt.add(new Gast(name, bett));
            buchungsliste.remove(a);
            aktuallisiereAnzeige();
        } catch (GastExceptions ex) {
            Logger.getLogger(programm.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jButtonBestaetigtActionPerformed

    private void jListAngefragtMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jListAngefragtMouseClicked
        //Bei Auswahl eines Eintrags aus der Liste wird die bestätigte Bettenanzahl mit der angefragten vorbelegt
        int select = jListAngefragt.getSelectedIndex();
        jTextFieldBettBestaetigt.setText(String.valueOf(buchungsliste.get(select).getBett()));
    }//GEN-LAST:event_jListAngefragtMouseClicked

    private void jMenuItemBearbeiten1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItemBearbeiten1ActionPerformed
        if (jListAngefragt.getSelectedIndex() == -1) {
            JOptionPane.showMessageDialog(rootPane, "Bitte einen angefragten Gast zum löschen auswählen.");
        } else {
            int loeschen = jListAngefragt.getSelectedIndex();
            String name = buchungsliste.get(loeschen).getName();

            int eingabe = JOptionPane.showConfirmDialog(rootPane, "Soll der Gast " + name + " wirklich gelöscht werden?", "Bestätigungsanfrage", JOptionPane.YES_NO_OPTION);
            if (eingabe == 0) {
                buchungsliste.remove(loeschen);
                aktuallisiereAnzeige();
            }

            
        }
    }//GEN-LAST:event_jMenuItemBearbeiten1ActionPerformed

